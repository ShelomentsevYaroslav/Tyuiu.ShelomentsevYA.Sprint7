    public partial class FormUserGuide_SYA : Form
    {
        public FormUserGuide_SYA()
        {
            InitializeComponent();
            LoadGuideText();
        }

        private void LoadGuideText()
        {
            richTextBoxGuide_SYA.Text =
@"РУКОВОДСТВО ПОЛЬЗОВАТЕЛЯ

1. Назначение программы
Программа «Отдел кадров» предназначена для работы с таблицами сотрудников,
хранящимися в формате CSV. Поддерживается просмотр, редактирование,
поиск и аналитика данных.

2. Загрузка данных
• Файл → Открыть CSV — открыть один CSV-файл
• Файл → Открыть папку CSV — загрузить все CSV из папки
• Переключение таблиц выполняется через выпадающий список

3. Работа с таблицей
• Добавить — добавляет пустую строку
• Изменить — включает / выключает режим редактирования
• Удалить — удаляет выбранные строки
• Обновить — перечитать CSV с диска

4. Поиск
Поиск выполняется по всем текстовым колонкам.
Для очистки нажмите кнопку ✕ справа от поля поиска.

5. Статистика
Раздел «Статистика» позволяет получить:
• Общую статистику
• Аналитику по подразделениям
• Аналитику по должностям
• Аналитику по образованию

6. Диаграммы
Диаграммы строятся по подразделениям:
• Средний оклад
• Количество сотрудников
• Фонд оплаты труда

7. Экспорт
Любую статистику можно экспортировать в CSV или TXT.

Примечание:
Для работы статистики требуется колонка «Оклад» с числовыми значениями.";
        }
    }
}
